class Pokemon{
    constructor(name, attack1, attack2, type, weight, exp, img, location){
        this.name = name;
        this.attack1 = attack1;
        this.attack2 = attack2;
        this.type = type;
        this.weight = weight;
        this.exp = exp;
        this.img = img;
        this.location = location;
    }
}

export default Pokemon